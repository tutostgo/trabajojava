package ejercicios;

public class Númeroparimpar {
    // Método principal
    public static void main(String[] args) {
        // Definir el número
        int a = 15;

        // Determina si el número es par o impar
        if (a % 2 == 0) {
        // muestra el resultado
            System.out.println(a + " es un número par.");
        } else {
        // muestra el resultado
            System.out.println(a + " es un número impar.");
        }
    }
}
