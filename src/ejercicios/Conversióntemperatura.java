package ejercicios;

public class Conversióntemperatura {
    // Método principal
    public static void main(String[] args) {
        // Definir la temperatura en Celsius
        int C = 25;

        // Convertir y mostrar la temperatura en Fahrenheit
        System.out.println(C + " °C es igual a " + ((C * 9.0 / 5) + 32) + " °F");
    }
}
